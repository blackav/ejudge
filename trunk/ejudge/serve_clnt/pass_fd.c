int
serve_clnt_pass_fd(int sock_fd, int nfd, int *fds)
{
  struct prot_serve_packet *out = 0;
  int out_size = 0;
  int r;

  out_size = sizeof(*out);
  out = alloca(out_size);
  out->id = SRV_CMD_PASS_FD;
  out->magic = PROT_SERVE_PACKET_MAGIC;
  if ((r = serve_clnt_send_packet(sock_fd, out_size, out)) < 0) return r;
  return serve_clnt_do_pass_fd(sock_fd, nfd, fds);
}

