#include "checker_internal.h"

void
checker_team_eof(void)
{
  fscanf(f_team, " ");
  if (ferror(f_team))
    fatal_CF("Input error from team output file");
  if (getc(f_team) != EOF)
    fatal_PE("Garbage in team output file");
}

